package ru.job4j.calculator;

public class Calculator {

    //метод складывает параметр first и second и возвращает значение.
    public double add(double first, double second) {
        return first + second;
    }
    //метод вычитает параметр first и second и возвращает значение.
    public double subtract(double first, double second) {
        return first - second;
    }
    //метод делит параметр first и second и возвращает значение.
    public double div(double first, double second) {
        return first / second;
    }
    //метод умножает параметр first и second и возвращает значение.
    public double multiple(double first, double second) {
        return first * second;
    }

}